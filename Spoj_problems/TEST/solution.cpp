#include<bits/stdc++.h>
using namespace std;
/* It's a simple problem focusing on I/O. 
Continuously take input from user until it reads the number 42 and print the numbers.
Problem Tag: #basic #tutorial #ad-hoc-1
*/
int main()
{
	int s;
	cin>>s;
	while(s!=42)
	{
		cout<<s<<endl;
		cin>>s;
	}
		
	return 0;
}
